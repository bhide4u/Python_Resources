# String Methods

my_string = "Hi, this is my String!!!"

# capitalize()
print(my_string.capitalize())
print(my_string)

# count()
print(my_string.count("i"))

# endswith()
print(my_string.endswith("s"))
print(my_string.endswith("!"))

# find()
print(my_string.find("!"))

# index()
print(my_string.index("!"))

# join()
print("_".join(my_string))

# lower()
print(my_string.lower())

# lstrip()
print("  Hi, this is my String!!!".lstrip())

# replace()
print("  Hi, this is my String!!!".replace("Hi" , "Hello").lstrip())

# rstrip()
print("Hi, this is my String!!!   ".rstrip())

# split()
print("Hi, this is my String!!!".split(" "))

# startswith()
print("Hi This is my string".startswith("Hi"))

# strip()
print("     Hi This is my string!!!   ".strip())

# title()
print("Hi this is my string!!!".title())

# upper()
print("Hi this is my string!!!".upper())

