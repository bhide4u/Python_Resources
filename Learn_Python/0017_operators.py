# Operators

# 1. Arithmetic Operators:

# Addition (+): Adds two operands.
print(2 + 3)

# Subtraction (-): Subtracts the second operand from the first operand.
print(3 - 2)

# Multiplication (*): Multiplies two operands.
print(2 * 3)

# Division (/): Divides the first operand by the second operand.
print(6 / 3)

# Modulo (%): Returns the remainder after division of the first operand by the second operand.
print(7 % 3)

# Floor Division (//): Returns the quotient after division of the first operand by the second operand, rounding down to the nearest integer.
print(7 // 3)

# Exponentiation (**): Raises the first operand to the power of the second operand.
print(2 ** 3)

# Operator Precedence -  Operator precedence is the order in which operations are evaluated in an expression. In programming languages, mathematical expressions often involve multiple operators, and operator precedence determines the order in which these operators are evaluated.


# 2. Assignment Operators:

# Simple Assignment (=): Assigns the value of the right-hand side to the left-hand side.
x = 5 # assigns the value 5 to the variable x.

# Add and Assignment (+=): Adds the value of the right-hand side to the value of the left-hand side, and assigns the result to the left-hand side.
x += 3 # is equivalent to x = x + 3.

# Subtract and Assignment (-=): Subtracts the value of the right-hand side from the value of the left-hand side, and assigns the result to the left-hand side.
x -= 3 # is equivalent to x = x - 3.

# Multiply and Assignment (*=): Multiplies the value of the right-hand side with the value of the left-hand side, and assigns the result to the left-hand side.
x *= 3 # is equivalent to x = x * 3.

# Divide and Assignment (/=): Divides the value of the left-hand side by the value of the right-hand side, and assigns the result to the left-hand side.
x /= 3 # is equivalent to x = x / 3.

# Modulo and Assignment (%=): Calculates the modulo of the left-hand side with the right-hand side, and assigns the result to the left-hand side.
x %= 3 # is equivalent to x = x % 3.

# 3. Comparison Operators:

# Equal to (==): Returns True if the values of the two operands are equal.
print(2 == 2) # returns True.

# Not equal to (!=): Returns True if the values of the two operands are not equal.
print(2 != 3) # returns True.

# Greater than (>): Returns True if the value of the left-hand side is greater than the value of the right-hand side.
print(3 > 2) # returns True.

# Less than (<): Returns True if the value of the left-hand side is less than the value of the right-hand side.
print(2 < 3) # returns True.

# Greater than or equal to (>=): Returns True if the value of the left-hand side is greater than or equal to the value of the right-hand side.
print(3 >= 2) # returns True.

# Less than or equal to (<=): Returns True if the value of the left-hand side is less than or equal to the value of the right-hand side.
print(2 <= 3) # returns True.

